import boto3
import uuid
from datetime import datetime

# Configuration
TABLE_NAME = "togs-and-dogs-prod-data"
PROFILE_NAME = "usmissionhero-website-prod"

session = boto3.Session(profile_name=PROFILE_NAME)
dynamodb = session.resource('dynamodb')
table = dynamodb.Table(TABLE_NAME)

ORPHAN_CLIENT_NAMES = ["Matthew Sync Test", "Matthew Final SWEEP"]

def repair_records():
    print(f"Starting targeted repair for: {ORPHAN_CLIENT_NAMES}")
    
    for name in ORPHAN_CLIENT_NAMES:
        print(f"\n--- Checking records for: {name} ---")
        
        # 1. Find the REQUEST
        response = table.scan(
            FilterExpression="entity_type = :et AND client_name = :name",
            ExpressionAttributeValues={":et": "REQUEST", ":name": name}
        )
        requests = response.get('Items', [])
        
        if not requests:
            print(f"No REQUEST found for {name}")
            continue
            
        for req in requests:
            req_id = req['PK'].replace('REQ#', '')
            client_id = req['SK'].replace('CLIENT#', '')
            existing_pet_id = req.get('pet_id')
            
            if existing_pet_id:
                print(f"Request {req_id} already has pet_id {existing_pet_id}. Skipping.")
                continue
                
            # 2. Create a new PET entity
            new_pet_id = str(uuid.uuid4())
            print(f"Creating new PET entity: {new_pet_id}")
            
            pet_item = {
                'PK': f"PET#{new_pet_id}",
                'SK': f"CLIENT#{client_id}",
                'entity_type': 'PET',
                'name': name,
                'client_id': client_id,
                'pet_id': new_pet_id,
                'care_instructions': req.get('pet_info', 'Repair generated'),
                'meet_and_greet_completed': True,
                'status': 'ACTIVE',
                'created_at': datetime.utcnow().isoformat(),
                'repair_log': f"Generated by repair script for orphan {name}"
            }
            
            table.put_item(Item=pet_item)
            
            # 3. Link back to REQUEST
            print(f"Linking PET# {new_pet_id} to REQUEST# {req_id}")
            table.update_item(
                Key={'PK': f"REQ#{req_id}", 'SK': f"CLIENT#{client_id}"},
                UpdateExpression="SET pet_id = :pid",
                ExpressionAttributeValues={":pid": new_pet_id}
            )
            
            # 4. Find and link to associated JOB(s)
            job_response = table.scan(
                FilterExpression="entity_type = :et AND request_id = :rid",
                ExpressionAttributeValues={":et": "JOB", ":rid": req_id}
            )
            jobs = job_response.get('Items', [])
            
            for job in jobs:
                job_id = job['PK'].replace('JOB#', '')
                job_sk = job['SK']
                print(f"Linking PET# {new_pet_id} to JOB# {job_id}")
                table.update_item(
                    Key={'PK': f"JOB#{job_id}", 'SK': job_sk},
                    UpdateExpression="SET pet_id = :pid",
                    ExpressionAttributeValues={":pid": new_pet_id}
                )
            
            print(f"SUCCESS: Repaired records for {name}. PET_ID: {new_pet_id}")

if __name__ == "__main__":
    repair_records()
